-- NUMERIC FUNCTIONS

-- abs(absolute value)
select abs(-5);
-- ceil
select ceil(29.6);
select ceil(20.1);
-- floor
select floor(27.8);
-- round
select round(20.1);
select round(20.8);
select round(20.5);
-- truncate
select truncate(2.456,2);
-- power
select power(2,3);
-- square root
select sqrt(16);
-- exp(exponent)
select exp(1);
-- log
select log(40);
-- log10
select log10(100);
-- mod(gives remainder)
select mod(5,2);
-- sign
select sign(-5);
select sign(5);
select sign(0);
-- pi
select pi();
-- radians(degrees to radians)
select radians(45);
-- degrees(radians to degrees)
select degrees(3.14159);
-- gratest
select gratest(10,20,30);
-- least
select least(10,20,30);
-- rand(random number)
select rand();
